package jdbc;

public class Vendor {
	private String v_id;//卖家id
	private String v_name;//卖家昵称
	private String v_email;//邮箱地址
	private String v_tel;//卖家电话
	private String v_sex;//卖家性别
	private int v_age;//卖家年龄
	private String v_picture;//卖家头像
	public String getV_id() {
		return v_id;
	}
	public void setV_id(String v_id) {
		this.v_id = v_id;
	}
	public String getV_name() {
		return v_name;
	}
	public void setV_name(String v_name) {
		this.v_name = v_name;
	}
	public String getV_email() {
		return v_email;
	}
	public void setV_email(String v_email) {
		this.v_email = v_email;
	}
	public String getV_tel() {
		return v_tel;
	}
	public void setV_tel(String v_tel) {
		this.v_tel = v_tel;
	}
	public String getV_sex() {
		return v_sex;
	}
	public void setV_sex(String v_sex) {
		this.v_sex = v_sex;
	}
	public int getV_age() {
		return v_age;
	}
	public void setV_age(int v_age) {
		this.v_age = v_age;
	}
	public String getV_picture() {
		return v_picture;
	}
	public void setV_picture(String v_picture) {
		this.v_picture = v_picture;
	}
	
}
