package jdbc;

public class Shopcar {
	private String g_id;//商品id
	private String c_id;//买家id
	private int sp_number;//商品数量
	public String getG_id() {
		return g_id;
	}
	public void setG_id(String g_id) {
		this.g_id = g_id;
	}
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public int getSp_number() {
		return sp_number;
	}
	public void setSp_number(int sp_number) {
		this.sp_number = sp_number;
	}
	
}
